        $(document).ready(function(){
            $('#typewriter').t({
                speed:100,
            });
            $('#typewriter').addClass("text-white");
            $("#send_message").click(function(e){
              $.toaster({ settings : {'timeout': 6000,} });
              e.preventDefault();
              $.ajax({
                url: "contact_process.php",
                method: "POST",
                data: $("#contact_us_form").serialize(),
                beforeSend: function(){
                  $("#send_message").attr("disabled", true);
                  $("#send_message").text("Sending...");
                },
                success: function(data){
                  if(data == "success"){
                    $.toaster({ 
                      title : 'Success!',
                      priority : 'success', 
                      message : 'Thanks, your message has been sent successfully'
                    });
                    setTimeout(function(){window.location.href= "./"}, 6000);
                  }else{
                    $.toaster({ 
                      title : 'Error!',
                      priority : 'danger', 
                      message : data
                    });
                  }
                  $("#send_message").attr("disabled", false);
                  $("#send_message").text("Send Message");
                }
              });
            });
        });
    </script>
